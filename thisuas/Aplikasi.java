    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        int row = tabel.rowAtPoint(evt.getPoint());
        String nopunggung = tabel.getValueAt(row, 0).toString();
        txt_nopunggung.setText(nopunggung);
        String nama = tabel.getValueAt(row, 1).toString();
        txt_nama.setText(nama);
        String alamat = tabel.getValueAt(row, 2).toString();
        txt_alamat.setText(alamat);
        String klub = tabel.getValueAt(row, 3).toString();
        txt_klub.setText(klub);
    }//GEN-LAST:event_tabelMouseClicked

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        try {
            String data = "DELETE FROM timnass WHERE no_punggung ='" + txt_nopunggung.getText() + "'";
            Connection con = (Connection) koneksi.getConnection();
            PreparedStatement pst = con.prepareStatement(data);
            pst.execute();
            
            DefaultTableModel tblModel = (DefaultTableModel)tabel.getModel();
        
        if(tabel.getSelectedRowCount()==1){
            tblModel.removeRow(tabel.getSelectedRow());
            JOptionPane.showMessageDialog(null, "Berhasil dihapus");
        }else{
            if(tabel.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "Tabel kosong!!!");
            }else{
                JOptionPane.showMessageDialog(this, "Harap pilih satu data!!!");
                 }
             }
        
        reset();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btndeleteActionPerformed


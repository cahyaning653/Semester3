    public void tabel(){
       DefaultTableModel tbl = new DefaultTableModel();
       tbl.addColumn("No Punggung"); 
       tbl.addColumn("Nama");
       tbl.addColumn("Alamat");
       tbl.addColumn("Klub");
       
        try {
            Statement st = (Statement) koneksi.getConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM timnass");
            
            while(rs.next()){
                tbl.addRow(new Object[] {
                    rs.getString("no_punggung"),
                    rs.getString("nama"),
                    rs.getString("alamat"),
                    rs.getString("klub")
                });
                tabel.setModel(tbl);
            }    
        } catch (Exception e) {
        }
    }
    

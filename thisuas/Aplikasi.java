    private void btninsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertActionPerformed
        // TODO add your handling code here:
        if(txt_nopunggung.getText().equals("")||txt_nama.getText().equals("")||txt_alamat.getText().equals("")||txt_klub.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Data tidak lengkap!!!");
        }else{
        try {
            String data = "INSERT INTO timnass VALUES ('" + txt_nopunggung.getText() + "','"
                                                            + txt_nama.getText() + "','"
                                                            + txt_alamat.getText() + "','"
                                                            + txt_klub.getText()
                                                            + "')";
            Connection con = (Connection) koneksi.getConnection();
            PreparedStatement pst = con.prepareStatement(data);
            pst.execute();
            
            JOptionPane.showMessageDialog(null, "Berhasil disimpan");
            
            reset();
                        
        } catch (Exception e) {
           JOptionPane.showMessageDialog(null, "Gagal disimpan " + e.getMessage());
        }
        }
        tabel();
    }//GEN-LAST:event_btninsertActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        if(txt_nopunggung.getText().equals("")||txt_nama.getText().equals("")||txt_alamat.getText().equals("")||txt_klub.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Data tidak lengkap!!!");
        
        }else{
        
        try {
            String data = "UPDATE timnass SET no_punggung ='" + txt_nopunggung.getText()
                                          + "',nama ='" + txt_nama.getText()
                                          + "',alamat ='" + txt_alamat.getText()
                                          + "',klub ='" + txt_klub.getText()
                                          + "'WHERE no_punggung ='" + txt_nopunggung.getText() + "'";
            Connection con = (Connection) koneksi.getConnection();
            PreparedStatement pst = con.prepareStatement(data);
            pst.execute();
            
        DefaultTableModel tblModel = (DefaultTableModel)tabel.getModel();
        if(tabel.getSelectedRowCount()==1){
                      
            tblModel.setValueAt(txt_nopunggung.getText(), tabel.getSelectedRow(), 0);
            tblModel.setValueAt(txt_nama.getText(), tabel.getSelectedRow(), 1);
            tblModel.setValueAt(txt_alamat.getText(), tabel.getSelectedRow(), 2);
            tblModel.setValueAt(txt_klub.getText(), tabel.getSelectedRow(), 3);
            
            JOptionPane.showMessageDialog(this, "Data berhasil diubah!!!");
        }else{
            if(tabel.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "Tabel kosong!!!");
            }else{
                JOptionPane.showMessageDialog(this, "Harap pilih satu data!!!");
                 }
             }

        reset();
        } catch (Exception e) {
        }
        }
    }//GEN-LAST:event_btnupdateActionPerformed


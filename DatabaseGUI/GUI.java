    private void tabellMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabellMouseClicked
        int row = tabell.rowAtPoint(evt.getPoint());
        String nim = tabell.getValueAt(row, 0).toString();
        in_nim.setText(nim);
        String nama = tabell.getValueAt(row, 1).toString();
        in_nama.setText(nama);
        String alamat = tabell.getValueAt(row, 2).toString();
        in_alamat.setText(alamat);
        String gender = tabell.getValueAt(row, 3).toString();
        if(gender.equals("P")){
            in_perempuan.setSelected(true);
        }else{
            in_lakilaki.setSelected(true);
        }
    }//GEN-LAST:event_tabellMouseClicked


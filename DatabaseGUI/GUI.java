    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        String gender = "";
        if (in_lakilaki.isSelected()) {
            gender = "L";
        } else if (in_perempuan.isSelected()) {
            gender = "P";
        }
        Insert in = new Insert();

        String nim = in_nim.getText();
        String nama = in_nama.getText();
        String alamat = in_alamat.getText();
        in.insert(nim, nama, alamat, gender);
        prog.setText(in.prog);
        tabel();
    }//GEN-LAST:event_SimpanActionPerformed

    private void PerbaruiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PerbaruiActionPerformed
        Connect conec = new Connect();
        conec.koneksiData();
        Update ubah = new Update();
        String nim = in_nim.getText();
        String nama = in_nama.getText();
        String alamat = in_alamat.getText();
        String gender = "";
        if (in_lakilaki.isSelected()) {
            gender = "L";
        } else if (in_perempuan.isSelected()) {
            gender = "P";
        }
        ubah.update(nim, nama, alamat, gender);
        prog.setText(ubah.prog);
        //update tampilan tabel
        tabel();
    }//GEN-LAST:event_PerbaruiActionPerformed

